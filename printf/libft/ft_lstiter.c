#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	int	n;

	n = ft_lstsize(lst);
	while (n > 0)
	{
		f(lst->content);
		lst = lst->next;
		n--;
	}
}
